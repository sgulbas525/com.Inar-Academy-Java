package chapters.chapter_02.Checkpoints02;

public class Checkpoint02_11{
	public static void main(String[] args){
		int a = 56 % 6;
		int b = 78 % -4;
		int c = -34 % 5;
		int d = -34 % -5;
		int e = 5 % 1;
		int f = 1 % 5;
		
		System.out.print("56 % 6 = " + a + "\n78 % -4 = " + b + "\n-34 % 5 = " + c + "\n-34 % -5 = " + d + "\n5 % 1 = " + e + "\n1 % 5 = " + f);
	}
}
